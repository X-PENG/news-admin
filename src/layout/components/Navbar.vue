<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <el-dropdown class="avatar-container" trigger="click">
        <div class="avatar-wrapper">
          <img :src="myAvatar3+'?imageView2/1/w/80/h/80'" class="user-avatar">
          <i class="el-icon-caret-bottom" />
        </div>
        <el-dropdown-menu slot="dropdown" class="user-dropdown">
          <router-link to="/private">
            <el-dropdown-item>
              个人中心
            </el-dropdown-item>
          </router-link>
          <router-link to="/dropDownMenu/newsDrafts" v-if="hasNewsDraftsRoute">
            <el-dropdown-item>
              草稿箱
            </el-dropdown-item>
          </router-link>          
          <el-dropdown-item divided @click.native="logout">
            <span style="display:block;">注销登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'

export default {
  data(){
    return {
      myAvatar1: undefined
    }
  },
  components: {
    Breadcrumb,
    Hamburger
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar',
      'hasNewsDraftsRoute'//是否具有草稿箱路由
    ]),
    myAvatar2(){
      if(!this.myAvatar1){
        //没有初始化过，就初始化一下
        // console.log('初始化头像2')
        //缓存下来
        this.myAvatar1 = this.avatar
      }
      return this.avatar
    },
    myAvatar3(){
        let result = !!this.myAvatar1 ?  this.myAvatar1 : this.myAvatar2
        return result
    }
  },
  methods: {
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      let targetRouteQueryStr =  JSON.stringify(this.$route.query)
      this.$router.push(`/login?redirect=${this.$route.fullPath}&targetRouteQueryStr=${targetRouteQueryStr}`)
    }
  }
}
</script>

